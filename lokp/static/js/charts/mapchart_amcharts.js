function createChartMap(areas) {
    AmCharts.makeChart( "map", {
        "type": "map",
        "colorSteps": 10,
        "dragMap": false,
        "projection": "eckert5",
        "areasSettings": {
            "autoZoom": false,
            "rollOverOutlineColor": "#CC0000",
            "outlineAlpha": 1,
            "outlineColor": "#FFFFFF",
            "outlineThickness": 1,
            "color": "#dedede",
            "balloonText": "[[title]]: [[value]]"
        },
        "dataProvider": {
            "map": "worldLow",
            "areas": areas
        },
        "zoomControl": {
            "panControlEnabled": false,
            "zoomControlEnabled": false,
            "homeButtonEnabled": false
        }
    });
}

function initContent(areas, attribute) {
    var countryList = $('#countries-list');
    countryList.html('');
    for (var i=0; i<areas.length; i++) {
        var country = '<li class="countries-list-entry">' + areas[i].name + '<span class="right">' + areas[i].value + ' ' + attribute + '</span></li>';
        countryList.append(country);
    }
}
